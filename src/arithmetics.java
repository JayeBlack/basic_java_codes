public class arithmetics {
    public static void main(String[] args) {
        int  num1 =20;
        int num2 = 30;

        double dec1 = 2.3;
        double dec2 = 3.5;
        //addition
        //subtraction
        //multiplication
        //division
        //remainders
        System.out.println("Int %Int: "+ (num1%num2));
        System.out.println("Dec %Dec: "+ (dec1%dec2));
        System.out.println("Dec %Int: "+ (dec1%num2));
        System.out.println(3*6%5+6/3-8);
    }
}
